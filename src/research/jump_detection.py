"""
src/research/jump_detection.py — Track B Phase 2
Jump score and EM soft separation for belief dynamics.

Model
-----
The logit return r_t is modelled as a mixture:

    r_t ~ (1-π) · N(0, σ_d²)  +  π · Laplace(0, b_j)

    diffusion component : N(0, σ_d²)       continuous Wiener noise
    jump component      : Laplace(0, b_j)  heavier-tailed jump arrivals

π      = prior jump probability (mixing weight, 0 < π < 1)
σ_d    = diffusion volatility (logit units per step)
b_j    = Laplace scale ≈ σ_j / sqrt(2)  (σ_j = typical jump size)

Why Laplace for jumps?
    Heavy-tailed relative to Normal; closed-form; analytically tractable.
    Equivalent to a double-exponential model common in jump-diffusion literature.

Soft EM assignment (E-step):
    p_jump(r) = P(jump | r)
              = π · f_jump(r) / [π · f_jump(r) + (1-π) · f_diff(r)]

Jump score (scalar summary):
    jump_score(r) = p_jump(r) ∈ [0, 1]
    > 0.5 → more likely a jump than diffusion
    > 0.9 → strong jump signal

No imports from Track A. Research only.
"""
from __future__ import annotations

import math
from dataclasses import dataclass
