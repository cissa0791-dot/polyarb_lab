def detect_jumps_in_series(
    returns: list[float],
    sigma_d: float = DIFF_SIGMA_INIT,
    b_j:    float = JUMP_LAPLACE_B,
    pi:     float = JUMP_PRIOR_PI,
    threshold: float = JUMP_SCORE_THRESH,
) -> list[JumpDetectionResult]:
    """
    Apply fixed-parameter jump detection to a full returns series.
    Does not update parameters — purely a classification pass.
    Useful for offline analysis / backtesting.
    """
    return [
        JumpDetectionResult(
            r=r,
            jump_score=jump_score(r, sigma_d=sigma_d, b_j=b_j, pi=pi),
            is_jump=is_jump(r, sigma_d=sigma_d, b_j=b_j, pi=pi, threshold=threshold),
            sigma_d=sigma_d,
            b_j=b_j,
            pi=pi,
            n_obs=i + 1,
        )
        for i, r in enumerate(returns)
    ]
